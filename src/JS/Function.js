


export const subcategoryName=(id)=>{
  if (id === "01") {
    return "Clay-made pottery";
  }
  if (id === "02") {
    return "Stoneware";
  }
  if (id === "03") {
    return "Porcelain";
  }
  if (id === "04") {
    return "Terra Cotta";
  }
  if (id === "05") {
    return "Ceramics & Architectural";
  }
  if (id === "06") {
    return "Home decor pottery";
  }
}